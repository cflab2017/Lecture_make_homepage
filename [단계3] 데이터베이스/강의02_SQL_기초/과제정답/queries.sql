-- ─────────────────────────────────────────
-- 쿼리 1. 가격 범위 필터
-- 50,000원 이상 200,000원 이하 상품을 가격 오름차순 정렬
-- BETWEEN을 사용하면 이상/이하 범위를 간결하게 표현 가능
-- ─────────────────────────────────────────
SELECT name, price, category
FROM products
WHERE price BETWEEN 50000 AND 200000  -- 50,000 ~ 200,000 범위 필터
ORDER BY price ASC;                   -- 가격 오름차순 정렬

-- ─────────────────────────────────────────
-- 쿼리 2. 카테고리 + 재고 복합 필터
-- 전자제품 중 재고 20개 이상인 상품
-- AND로 두 조건을 동시에 적용
-- ─────────────────────────────────────────
SELECT name, price, stock
FROM products
WHERE category = '전자제품'  -- 카테고리가 전자제품
  AND stock >= 20;           -- 재고 20개 이상

-- ─────────────────────────────────────────
-- 쿼리 3. 품절 상품 조회
-- stock이 0인 상품 → 재고 없음 = 품절
-- ─────────────────────────────────────────
SELECT name, category
FROM products
WHERE stock = 0;  -- 재고가 정확히 0인 상품

-- ─────────────────────────────────────────
-- 쿼리 4. 평점 필터 + 정렬 + 개수 제한
-- 평점 4.5 이상을 내림차순 정렬 후 상위 3개
-- ORDER BY + LIMIT 조합으로 순위 조회
-- ─────────────────────────────────────────
SELECT name, rating, price
FROM products
WHERE rating >= 4.5        -- 평점 4.5 이상
ORDER BY rating DESC       -- 높은 평점 먼저
LIMIT 3;                   -- 상위 3개만

-- ─────────────────────────────────────────
-- 쿼리 5. 이름 패턴 검색
-- '스'가 포함된 상품명 검색
-- LIKE '%스%': 앞뒤에 무엇이 오든 '스'가 들어가면 매칭
-- ─────────────────────────────────────────
SELECT name, price, category
FROM products
WHERE name LIKE '%스%';  -- 이름에 '스'가 포함된 상품
